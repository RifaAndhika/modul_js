//Operator Comparison/perbandingan

//mengecek atau memandingkan persamaan nilai

let a = 10;
let b = "10";
console.log(a == b); // true


//mengecek atau memandingkan persamaan nilai dan tipe data
let c = 10; // number
let d = "10"; // string
console.log(c === d); // false


//mengecek ketidak samaan  nilai dari variabel
let e = 10; // number
let f = "10"; // string
console.log(e != f); // false  


//mengecek atau membandingkan ketidak samaan nilai dan tipe data
let g = 10; // number
let h = "10"; // string
console.log(g !== h); // true

// > mengecek nilai lebih besar
let i = 10;
let j = 5;
console.log(i > j); // true

// < mengecek nilai lebih kecil
let k = 10;
let l = 5;
console.log(k < l); // false

// >= mengecek nilai lebih besar sama dengan
let m = 10;
let n = 10;
console.log(m >= n); // true

// <= mengecek nilai lebih kecil sama dengan
let o = 11;
let p = 10;
console.log(o <= p); // false
